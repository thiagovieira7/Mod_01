// Exemplo com do while

// Primeiro a ação é executado e depois a condição é testada

let result = "";
let i = 0;

do {
  i = i + 1;
  result = result + i;
} while (i < 5);

console.log("Resultado do DO WHILE: " + result);

// Exemplo While

let resultado = "";
let x = 0;

while (x < 5) {
  x = x + 1;
  resultado = resultado + x;
  console.log("Resultado do WHILE: " + resultado);
}
