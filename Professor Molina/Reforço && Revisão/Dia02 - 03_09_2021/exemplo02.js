// Laço for...in.... : retorna o indice (posição do elemento) da lista
// Depois do for, preciso criar uma variável
// Depois do in coloco o nome da lista que quero trabalhar

let letras = ["a", "b", "c", "d", "e"]; // Criamos uma lista com letras
for (let i in letras) {
  console.log(i);
}

// Laço for...of.... : retorna o conteúdo da lista
// Depois do for, preciso criar uma variável
// Depois do of coloco o nome da lista que quero trabalhar

let letras2 = ["a", "b", "c", "d", "e"];
for (let i of letras2) {
  console.log(i);
}
