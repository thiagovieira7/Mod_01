function conta(valor1, valor2) {
  console.log(
    `A soma de ${valor1} e ${valor2} é igual a : ` + (valor1 + valor2)
  );
  console.log(
    `A subtração de ${valor1} e ${valor2} é igual a : ` + (valor1 - valor2)
  );
  console.log(
    `A divisão de ${valor1} e ${valor2} é igual a : ` + valor1 / valor2
  );
  console.log(
    `A multiplicação de ${valor1} e ${valor2} é igual a : ` + valor1 * valor2
  );
}
conta(2, 4);
